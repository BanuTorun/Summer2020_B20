package day06_ComparisonOperators;

public class swap2 {
    public static void main(String[] args) {
         int a=10;
         int b=15;


        a=a+b;                          // a=25
        b=a-b;                          // b= 10
        a=a-b;                          // a=15
        System.out.println("a = "+a);   // a=15
        System.out.println("b = "+b);   // b=10

        
    }
}
