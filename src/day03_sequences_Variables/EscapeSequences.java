package day03_sequences_Variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello Everyone\nMy Name is Cybertek.");     // \n new line
        System.out.println("\tI am in Virginia");                       // \t give space inside ""
        System.out.println("\n\n\t\t\tI Love programming");             // \n\n give two new line

        System.out.println("\\");               // this print me one back slash
        System.out.println("\\\\");             //
        System.out.println("/");                // this print me on front slash
        System.out.println("//");               //

        System.out.println("My name is Ba \'nu\'"); // 'Banu'
        System.out.println("My name is \'Banu\'");
        System.out.println("My name is 'Banu'" );

        System.out.println("My favorite TV-Series: \"Game Of Thrones\"");


    }

}
